age = int(input("กรุณาป้อนอายุ: "))

if age >= 0 and age <= 12:
    print("อายุของคุณคือ :",age,"ปี")
    print("ช่วงอายุของคุณ: เด็ก")
elif age >= 13 and age <= 19:
    print("อายุของคุณคือ :",age,"ปี")
    print("ช่วงอายุของคุณ: วัยรุ่น")
elif age >= 20 and age <= 59:
    print("อายุของคุณคือ :",age,"ปี")
    print("ช่วงอายุของคุณ: ผู้ใหญ่")
else:
    print("อายุของคุณคือ :",age,"ปี")
    print("ช่วงอายุของคุณ: สูงอายุ")
